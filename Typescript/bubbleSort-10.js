// Bubble Sort Algorithm
function bubbleSort(arr) {
    var _a;
    var n = arr.length;
    var swapped;
    do {
        swapped = false;
        for (var i = 0; i < n - 1; i++) {
            if (arr[i] > arr[i + 1]) {
                // Swap elements
                _a = [arr[i + 1], arr[i]], arr[i] = _a[0], arr[i + 1] = _a[1];
                swapped = true;
            }
        }
        n--; // Reduce the number of elements to check
    } while (swapped);
    return arr;
}
